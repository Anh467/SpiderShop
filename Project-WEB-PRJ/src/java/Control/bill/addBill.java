    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("name");
        String phone = request.getParameter("phone");
        String address = request.getParameter("address");   
        String userId[] = request.getParameterValues("BillUserId");
        String AniId[] = request.getParameterValues("BillAniId");
        AddressUser a= new AddressUser(userId[0], phone, address, name);
        if (a.find()<0){
            new model.GetInfor().CreateAddressAccID(userId[0], phone, address, name);
        }
        BillAdd e= new BillAdd(userId[0], phone, address, name);
        e.add();
        BillDetailList bill= new BillDetailList(userId[0], e.getId(), AniId);
        response.sendRedirect("cart.jsp");
    }

